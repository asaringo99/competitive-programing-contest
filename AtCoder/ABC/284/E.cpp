void dfs(int v, int prev, int &res){
    used[v] = true;
    res++;
    if(res >= 1000000) return;
    for(int u : G[v]){
        if(u == prev) continue;
        if(used[u]) continue;
        dfs(u,v,res);
    }
    used[v] = false;
}

